// index.html
function checkRadio() {
    var htm = document.getElementById("html");
    var cs = document.getElementById("css");
    var java = document.getElementById("javascript");
    if (htm.checked) {
        window.open("Html/html1.html");
    }
    else if (cs.checked) {
        window.open("Css/css1.html");
    }
    else if (java.checked) {
        window.open("Javascript/javascript1.html");
    }
}

